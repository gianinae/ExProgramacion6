CREATE PROCEDURE [dbo].[PuestosEliminar]
@Id_Puesto INT
AS
BEGIN
	SET NOCOUNT ON 
	BEGIN TRANSACTION TRASA
	
	BEGIN TRY
--Metodo
		DELETE FROM Puestos
		WHERE Id_Puesto = @Id_Puesto
--FinMetodo
	COMMIT TRANSACTION TRASA

		SELECT 0 AS CodeError, '' AS MsgError

	END TRY 

	BEGIN CATCH
		SELECT 
			ERROR_NUMBER() AS CodeError
			,ERROR_MESSAGE() AS MsgErrror
	END CATCH


END

